return {
	"folke/snacks.nvim",
	priority = 1000,
	lazy = false,
	---@type snacks.Config
	opts = {
		bigfile = { enabled = true },
		dashboard = { enabled = true },
		indent = { enabled = true },
		input = { enabled = true },
		lazygit = { enabled = true },
		picker = {
			enabled = true,
			on_key = function(key, picker)
				local preview_win = picker.preview and picker.preview.win
				if not preview_win then
					return false
				end

				if key == "<C-d>" or key == "<C-u>" then
					-- Prevent the key from affecting the fuzzy search
					vim.api.nvim_win_call(preview_win, function()
						vim.cmd("normal! " .. key)
					end)
					return true -- IMPORTANT: tells Snacks to NOT handle the key any further
				end

				return false
			end,
			buffers = {
				-- The 'keys' table holds the keymaps for this specific picker
				keys = {
					-- Define the keymap for normal mode
					n = {
						-- Map the 'd' key to the 'delete_buffer' action
						["<C-x>"] = "delete_buffer",
					},
					-- Define the keymap for insert mode (if you want to delete from there)
					i = {
						["<C-d>"] = "delete_buffer",
					},
				},
			},
		},
		notifier = { enabled = true },
		quickfile = { enabled = true },
		scope = { enabled = true },
		scroll = { enabled = false },
		statuscolumn = { enabled = true },
		words = { enabled = true },
		zen = { enabled = true },
	},
	keys = {
		-- find
		{
			"<leader>fb",
			function()
				Snacks.picker.buffers()
			end,
			desc = "Buffers",
		},
		{
			"<leader>fc",
			function()
				Snacks.picker.files({ cwd = vim.fn.stdpath("config") })
			end,
